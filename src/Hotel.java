
public class Hotel {

	private int hotel_rent;
	private int hotel_cost;
	
	public Hotel(int hotel_rent,int hotel_cost){
		
		this.hotel_rent=hotel_rent;
		this.hotel_cost=hotel_cost;
	
	}

	public int getHotel_rent() {
		return hotel_rent;
	}

	public int getHotel_cost() {
		return hotel_cost;
	}
	
}
